# Función Range o Rango

funcionRango = range(1,11,1)
# Range (Número inicial del rango, Número final del rango sin el número final, Incremento del rango)

# Ejemplos

rango1 = range (1,31,1)
rango2 = range (1,31,2)
rango3 = range (1,31,3)

# Imprimir cada rango para ver como se crearon cada uno de ellos

# Rango 1
print("\nRango 1")
for i in rango1:
    print(i,end=" ")

# Rango 2
print("\nRango 2")
for i in rango2:
    print(i,end=" ")

# Rango 3
print("\nRango 3")
for i in rango3:
    print(i,end=" ")
print("\n")